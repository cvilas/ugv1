//-----------------------------------------------------------------------------
bool IoBoardReply::verifyChecksum()
//-----------------------------------------------------------------------------
{
    const_iterator itCSum = end() - 2; // checksum is second last character

    int csum = 0;
    const_iterator it = begin();
    while( it != itCSum )
    {
        csum += (int)(*it);
        ++it;
    }

    return ( (*itCSum) == (csum&0xFF) );

}

